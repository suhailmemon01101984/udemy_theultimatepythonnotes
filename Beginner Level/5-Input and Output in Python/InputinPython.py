# Input in Python

# There are a few ways to read input in Python. 
# The simplest way is to use the input() function, 
# which allows you to prompt the user for input and store their response in a variable.

# Here's an example:

# Example 1: Reading input from the user
name = input("What's your name? ")
print(f"Hello, {name}!")

# Example 2: Reading numerical input from the user
age = int(input("How old are you? "))
print(f"You will be {age+1} years old next year!")
