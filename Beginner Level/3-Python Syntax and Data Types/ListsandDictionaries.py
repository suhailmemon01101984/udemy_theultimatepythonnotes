#Lists and Dictionaries

'''
Lists and dictionaries are two important data structures in Python.

A list is a collection of values, which can be of any data type. 
Lists are created using square brackets and separated by commas. 

For example:
'''

my_list = [1, 2, 3, "four", "five"]

print(my_list[0]) #This will print the first array which is 1  
print(my_list[3]) #This will print the forth array which is 4  




my_dict = {"name": "John", "age": 25, "is_student": True}

print(my_dict) #This will print the first array which is 1 

