#Basic Operations

'''
Python supports several basic operations on variables and data types, including:

Arithmetic operations: 
Addition (+), 
subtraction (-), 
multiplication (*), 
division (/), 
and modulus (%).

String concatenation: 
Combining two strings using the plus (+) operator.

Comparison operators: 
Comparing two values using less than (<),
greater than (>),
less than or equal to (<=),
greater than or equal to (>=), equal to (==), and not equal to (!=).


Here's an example of how to use some of these operations:
'''

x = 7
y = 1
z = x + y
print(z) # Output: 8

message = "Hi, " + "World!"
print(message) # Output: "Hello, World!" | Because we add both strings

is_greater = x > y
print(is_greater) # Output: True | Because we are testing if x is greater than y



# Define two variables "x" and "y" with numerical values
x = 42
y = 3.14

# Perform arithmetic operations on "x" and "y" and print the results to the console
print(x + y)  # Addition
print(x - y)  # Subtraction
print(x * y)  # Multiplication
print(x / y)  # Division
print(x % y)  # Modulo (remainder of division)
print(x // y) # Integer division (rounded down to nearest integer)
print(x ** y) # Exponentiation

# Define two variables "b" and "c" with string values
b = "Hello"
c = "world"

# Concatenate strings and print the result to the console
print(b + ", " + c + "!")

# Multiply a string and print the result to the console
print(b * 3)




