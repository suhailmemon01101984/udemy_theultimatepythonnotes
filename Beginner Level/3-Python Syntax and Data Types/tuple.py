# Tuple

'''
Tuples are similar to lists in Python, 
but they are immutable, which means that once they are created, 
their contents cannot be changed. This can be useful when you want to create a 
collection of values that should not be modified later on in your program.
'''

# Creating a tuple
my_tuple = ("apple", "Mango", "cherry")

# Accessing elements of a tuple
print(my_tuple[0])  # Output: "apple"
print(my_tuple[1])  # Output: "banana"
print(my_tuple[2])  # Output: "cherry"
