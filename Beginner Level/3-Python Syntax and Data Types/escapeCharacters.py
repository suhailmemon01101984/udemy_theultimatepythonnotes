# Using Escape Characters in Python

# New line character
print("Hello\nworld!")

# Tab character
print("Hello\tworld!")

# Backslash character
print("Hello\\world!")

# Single quote character
print('She said, "Hello!"')

# Double quote character
print("She said, \"Hello!\"")


