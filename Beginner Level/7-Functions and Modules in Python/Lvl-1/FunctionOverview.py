"""
Functions in Python are blocks of code that perform a 
specific task. They allow you to break down your program into smaller, 
more manageable pieces of code. Functions can take inputs, called arguments, 
and return outputs, or they can simply perform a task without returning anything.
"""

#Here's an example of a simple function that takes two arguments and returns their sum:

def add_numbers(x, y):
    return x + y

#This function takes two arguments, x and y, 
# adds them together using the + operator, and returns the result. 

# You can call this function like this:

result = add_numbers(5, 10) #Change the number and observe the result
print(result)  # Output: 15




