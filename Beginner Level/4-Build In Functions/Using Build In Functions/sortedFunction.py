#sorted(): sorts the elements of an iterable in ascending order and returns a new list.

numbers = [5, 2, 8, 1, 3]
sorted_numbers = sorted(numbers)
print(sorted_numbers) # Output: [1, 2, 3, 5, 8]

string = "hello world"
sorted_string = sorted(string)
print(sorted_string) # Output: [' ', 'd', 'e', 'h', 'l', 'l', 'o', 'o', 'r', 'w']
