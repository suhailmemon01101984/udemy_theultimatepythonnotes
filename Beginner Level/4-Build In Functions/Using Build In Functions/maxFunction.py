'''
max(): returns the largest element in an iterable (list, tuple, set, etc.) or the 
largest of two or more arguments.

Example usage:

'''

numbers = [1, 3, 5, 7, 9]
max_number = max(numbers)
print(max_number) # Output: 9

max_value = max(10, 20, 30)
print(max_value) # Output: 30

