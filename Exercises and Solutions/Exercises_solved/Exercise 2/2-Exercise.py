# Exercise: four functions to add, subtract, multiply, and divide two numbers:

'''
Write 4 function 
to add, subtract, multiply, and divide two numbers:
'''

def add_two_numbers(num1,num2):
    return(num1+num2)

def subtract_two_numbers(num1,num2):
    return(num1-num2)

def multiply_two_numbers(num1,num2):
    return(num1*num2)

def divide_two_numbers(num1,num2):
    return(num1/num2)

print(add_two_numbers(10,5))
print(subtract_two_numbers(5,10))
print(multiply_two_numbers(5,10))
print(divide_two_numbers(1723,4))
print(divide_two_numbers(4,1024))
